	<p>The hills behind the Clubhouse are home to one of the finest archery courses found in the state. The 30-station course is open to both members and non-members. There is also a 3-D course on the site of the field course, consisting of 30 targets. Each target is sponsored by an individual or organization, and annually there are opportunities to help keep this course up to date. Check in with one of the league members for more information. </p>

	<p>The 3-D course is set up twice each week during the season for league shooting. The Club offers a 3-D league, a Hunter League, and a woman’s and youth league. Shooting starts in May of each year. </p>

	<p>Targets and ranges are maintained through the volunteer assistance of Club members, and help is always both welcome and appreciated in an effort to keep the course looking it’s finest. If you take something on the course to eat or drink while you’re shooting, please take all garbage out with you to designated disposal sites. Enjoy your outdoor adventure in the natural beauty of the Club woods!!</p>

