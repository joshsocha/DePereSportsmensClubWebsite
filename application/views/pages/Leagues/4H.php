	<p>The Brown County 4-H Shooting Sports program offers opportunities for youth to learn more about shooting sports in the safe and controlled environment of the Club grounds. Meeting the mission of the Club to engage youth in a variety of shooting sports, the 4-H program personifies this mission by providing access to a whole new group of future shooters. This program is organized through the University of Wisconsin-Extension (UWEX), and follows practices and procedures consistent with good shooting safety and development of safe gun handling habits and skills. Currently, almost 200 youth participate in this event each year. This program also serves as a feeder program to the Junior Rifle Program profiled elsewhere in this website.</p>

