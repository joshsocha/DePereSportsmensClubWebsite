		<p>The Brown County 4-H Shooting Sports program offers opportunities for youth to learn about shooting sports in the safe and controlled environment of the Club grounds. Meeting the mission of the Club to engage youth in a variety of shooting sports, the 4-H program personifies this mission by providing access to a whole new group of future shooters. This program is organized through the University of Wisconsin-Extension (UWEX). Adult volunteer instructors are required to successfully complete a twelve-hour certification workshop before they are approved to teach youth. Our educational focus is on life skills development, safety procedures and responsible use of firearms and archery equipment.</p>

		<p>Brown County 4-H Shooting Sports is geared for teaching beginning target shooting skills for youth at junior, intermediate and senior levels. Beginners learn basic skills through the County 4-H Shooting Sports program, advance to fun shoots within the state, and have the opportunity to make the Wisconsin National 4-H Shooting Sports Team. WI 4-H national team competes in the National 4-H Championships at Grand Island, Nebraska.</p>

		<p>Interested in shooting sports, hunting and wildlife? The Wisconsin 4-H Shooting Sports program is open to 4-H youth ages 9 (must be in 3rd grade) to 19. Registration is from October through November on the 4-H Website <a href="https://4h.extension.wisc.edu/join-4-h/" target="_blank">https://4h.extension.wisc.edu/join-4-h/</a> Season starts in January with 8-10 practices on Saturdays till June. For any questions, please email <a href="mailto:bc4hsswi@gmail.com">bc4hsswi@gmail.com</a>.</p>

		<p>Disciplines offered are air rifle, .22 rifle, air pistol, .22 pistol, archery, shotgun, and wildlife. All equipment is provided for the youth.</p>

		<p>Currently, almost 200 youth participate in this event each year. This program also serves as a feeder program to the <a href="<?php echo base_url();?>Leagues/JuniorRifle">Junior Rifle</a> Program.</p>
