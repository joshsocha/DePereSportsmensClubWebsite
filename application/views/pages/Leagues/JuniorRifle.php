	<h2>About Junior Rifle</h2>
	<p>These young shooters are instructed in rifle safety and marksmanship along with the correct fundamentals for competitive shooting by certified coaches. There are various shooting competitions around the state that the Juniors are able to compete in. All equipment used is provided by the team including .22 cal. competition-grade target and air rifles, ammunition, and targets. The Juniors earn NRA awards during the season as their skills improve. At season's end, the team sponsors a banquet and awards trophies to top competitors in each age category. The Junior Rifle program is an excellent opportunity to promote safe firearm handling, and to get youth interested in shooting sports and prepare them for adult membership with its benefits and responsibilities.</p>
