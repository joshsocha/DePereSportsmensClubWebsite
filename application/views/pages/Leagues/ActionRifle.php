	<p>Action Rifle is set up for everyone to compete using any kind of 22 rifle. There is a class for everyone. We shoot every other Tuesday night opposite of Action Pistol league. Only 22 rifles can be used to compete. All range rules apply and are strictly enforced.</p>

	<h3>Organization</h3>
	<p>This league is intended for shooters of all skill levels and 22 equipment types.</p>

	<h3>Rules</h3>
	<p>All club range rules apply. Scoring will be covered before each league night starts.</p>

	<h3>Schedule</h3>
	<p>Every other Tuesday night opposite Action Pistol league. Please check club calendar. </p>

	<h3>Equipment</h3>
	<p>A 22 rifle spotting scope and/or binoclears are helpful. At least 50 rounds of 22 ammo required. Eye and ear protection recommended.</p>

	<h3>Operation</h3>
	<p>We start league set up at 4:30 p.m. Shooting starts at 5:00 p.m.</p>

	<h3>Fees</h3>
	<span>$5.00/night for members </span><br/>
