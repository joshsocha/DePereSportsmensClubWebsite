	<h2>Operations</h2>
	<p>Starting first week of April at 4:00pm, check-in and pay range fees:</p>
	<ul>
		<li>$7 members and/or Safety Officers</li>
		<li>$10 non-members</li>
	</ul>
	
	<p>From this point onward, eye and ear protection is mandatory. There will be a "gear-up" station where you can put on your gear and load magazines (pistols unloaded and cased) and then onto the"gun-up" or safety area where the pistol is uncased, still unloaded, is holstered.</p>
	
	<p>** NO AMMUNITION IN THE SAFETY AREA**</p>
	
	<p>Not in pockets, not in any magazine/speed loaders that you may be carrying. From there proceed to the firing line. At each stage, the course of fire will be explained. Once on the firing line the shooter will be asked if they understand the course of fire. Upon the affirmative, the shooter will be told to "Load and Make Ready", the shooter will withdraw their pistol, load, and then hold in a"Low Ready" position until instructed to "Holster Safe" placing on the firearm's safety (if applicable). The shooter will be given a "Shooter Ready?" call and the timer starts. To start with we will use a 3-second delay. Once the buzzer sounds, the shooter may draw and shoot the course of fire. After the last shot, the shooter will take a "Low Ready" position until told to "Unload" (remove magazine) and if the slide is closed, the slide should be locked back and visually checked for being unloaded. Then the command to "Slide Forward and Hammer down" is given, the slide is released and trigger is pulled to verify the firearm is empty. Finally the command to "Holster Safe". One the command "Line Safe" is given you may police your gear and tape targets.</p>

	<p>The above is a generalization of the commands and there may be slight variations based on the stage/firearm/safety officer but the basic goal is safety.</p>

	<h2>Remember</h2>
	<p><b>We are operating a "cold range"</b> which means that all firearms are unloaded and holstered at ALL times until directed by a safety officer on the firing line. <b>Muzzle discipline will be strictly enforced.</b> Each stage will have defined"muzzle safe points" where the muzzle must be kept inside. Finger/trigger discipline will be strictly enforced (finger not on trigger until sights on target)</p>

