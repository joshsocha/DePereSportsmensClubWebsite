	<h2>Organization</h2>
	<p>The league is intended as a practice league for all types of action pistol shooting, IDPA, and IPSC. Everyone is welcome to shoot regardless of club affiliation and does not need to be a DePere Sportsmen's club member but would be welcomed to join if interested.</p>

	<h2>Rules</h2>
	<p>Overall we will generally use IDPA rules for safety, gun-handling rules and target scoring (simpler than IPSC). Please take the time to go thru the IDPA rule book (see link below). Scoring  will be the honor system, and those who do not wish to have their scores published do not have to. If you would prefer to use only your first name, or an alias, that is fine as well. Scores will initially be emailed to the distribution list, and eventually posted on the club web site.</p>

	<h2>Scheduling</h2>
	<p>The league will shoot every Tuesday, starting the first week of April and running thru mid-September. Shooting will start at 4:30pm.</p>

	<h2>Equipment</h2>
	<p>There are two groups of shooters, those that do/may compete in their respective disciplines and those that just want to shoot. For the competitors, and those that think they may want to, it is up to the individual shooters to make sure that their equipment meets the requirements of their disciplines. For those that want to just shoot, here is the basic equipment: </p>
	<ul>
		<li>Good quality firearm - Center fire revolver with at least 3 speed loaders or Semi- Automatic with at least 3 magazines.</li>
		<li>We will try to accommodate .22 rim-fire for each event â€“ no holster is needed as we will shoot from low ready.</li>
		<li>Strong side belt holster (no cross-draw, shoulder, ankle, etc) , again no cross draw. See RESOURCES below.</li>
		<li>2 Magazine/speed loader pouches (I'd recommend at least three)</li>
		<li>Stiff Belt at least 1 1/2" wide or wider if your holster/pouches will accept it.</li>
